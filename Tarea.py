# Ejemplo 1:
palabra = input("Ingrese una palabra: ")

print("\nEjemplo 1")
print("El tipo de dato de 'palabra' es:", type(palabra))
print("La palabra", palabra, "tiene", len(palabra), "caracteres")
print("El primer carácter es:", palabra[0])
print("El último carácter es:", palabra[-1])

# Ejemplo 2:
frase = input("\nIngrese una frase: ")

print("\nEjemplo 2")
print("El tipo de dato de 'frase' es:", type(frase))
print("La frase completa es:", frase)
print("La frase tiene", len(frase), "caracteres")
print("Los primeros 5 caracteres son:", frase[0:5])
